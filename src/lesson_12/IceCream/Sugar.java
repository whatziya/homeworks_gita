package lesson_12.IceCream;

public class Sugar{
    private int mass;
    private int price;

    public Sugar(int mass, int price) {
        this.mass = mass;
        this.price = price;
    }

    public int getMass() {
        return mass;
    }

    public int getPrice() {
        return price;
    }

    public void setMass(int mass) {
        this.mass = mass;
    }
}
